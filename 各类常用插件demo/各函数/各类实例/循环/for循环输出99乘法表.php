<?php
	/*
	 *    1*1=1
	 *    1*2=2  2*2=4
	 *    1*3=3  2*3=6  3*3=9
	 *    1*4=4  2*4=8  3*4=12  4*4=16 
	 * 
	 */

	//每一行当中都是从1开始,循环输出9个br
	for($i=1;$i<=9;$i++){
		//每一行输出的表达式的个数就是行号
		for($j=1;$j<=$i;$j++){
			echo $j."*".$i."=".($i*$j)."&nbsp;&nbsp;";
		}
		echo "<br />"; 
	}
	
	
	